package vn.hkd.model;

public class DoiTac {
	private long idDoiTac;
	private String tenDoiTac;
	private String truSoDoiTac;
	private String vanPhongDoiTac;
	private String sdtDoiTac;
	private String emailDoiTac;
	private String websiteDoiTac;
	private String bdDoiTac;
	private String ktDoiTac;
	public DoiTac() {
		super();
	}
	public DoiTac(long idDoiTac, String tenDoiTac, String truSoDoiTac, String vanPhongDoiTac, String sdtDoiTac,
			String emailDoiTac, String websiteDoiTac, String bdDoiTac, String ktDoiTac) {
		super();
		this.idDoiTac = idDoiTac;
		this.tenDoiTac = tenDoiTac;
		this.truSoDoiTac = truSoDoiTac;
		this.vanPhongDoiTac = vanPhongDoiTac;
		this.sdtDoiTac = sdtDoiTac;
		this.emailDoiTac = emailDoiTac;
		this.websiteDoiTac = websiteDoiTac;
		this.bdDoiTac = bdDoiTac;
		this.ktDoiTac = ktDoiTac;
	}
	public long getIdDoiTac() {
		return idDoiTac;
	}
	public void setIdDoiTac(long idDoiTac) {
		this.idDoiTac = idDoiTac;
	}
	public String getTenDoiTac() {
		return tenDoiTac;
	}
	public void setTenDoiTac(String tenDoiTac) {
		this.tenDoiTac = tenDoiTac;
	}
	public String getTruSoDoiTac() {
		return truSoDoiTac;
	}
	public void setTruSoDoiTac(String truSoDoiTac) {
		this.truSoDoiTac = truSoDoiTac;
	}
	public String getVanPhongDoiTac() {
		return vanPhongDoiTac;
	}
	public void setVanPhongDoiTac(String vanPhongDoiTac) {
		this.vanPhongDoiTac = vanPhongDoiTac;
	}
	public String getSdtDoiTac() {
		return sdtDoiTac;
	}
	public void setSdtDoiTac(String sdtDoiTac) {
		this.sdtDoiTac = sdtDoiTac;
	}
	public String getEmailDoiTac() {
		return emailDoiTac;
	}
	public void setEmailDoiTac(String emailDoiTac) {
		this.emailDoiTac = emailDoiTac;
	}
	public String getWebsiteDoiTac() {
		return websiteDoiTac;
	}
	public void setWebsiteDoiTac(String websiteDoiTac) {
		this.websiteDoiTac = websiteDoiTac;
	}
	public String getBdDoiTac() {
		return bdDoiTac;
	}
	public void setBdDoiTac(String bdDoiTac) {
		this.bdDoiTac = bdDoiTac;
	}
	public String getKtDoiTac() {
		return ktDoiTac;
	}
	public void setKtDoiTac(String ktDoiTac) {
		this.ktDoiTac = ktDoiTac;
	}
	
}
